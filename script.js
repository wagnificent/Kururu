// Function to update content
function updateContent() {
    document.querySelectorAll('[data-i18n]').forEach(function(element) {
        const key = element.getAttribute('data-i18n');
        if (key.includes('[placeholder]')) {
            const placeholderKey = key.replace('[placeholder]', '');
            element.placeholder = i18next.t(placeholderKey);
        } else {
            element.innerHTML = i18next.t(key);
        }
    });
}

// Language switcher
document.getElementById('language-select').addEventListener('change', function() {
    const selectedLang = this.value;
    i18next.changeLanguage(selectedLang, function(err, t) {
        updateContent();
    });
});

// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth'
            });
        }
    });
});

// Form submission handler
document.querySelector('form').addEventListener('submit', function(e) {
    e.preventDefault();
    alert(i18next.t('submit') + ' sent! We will get back to you soon.');
    this.reset();
});

// Newsletter subscription
document.querySelector('.newsletter button').addEventListener('click', function() {
    const email = document.querySelector('.newsletter input').value;
    if (email) {
        alert(i18next.t('newsletter_button') + 'd! Stay tuned for exclusive Kururu deals.');
        document.querySelector('.newsletter input').value = '';
    } else {
        alert('Please enter a valid email.');
    }
});
